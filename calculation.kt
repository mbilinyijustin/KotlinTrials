fun main(){
    val number = 10
    if (number > 0){
        println("$number is positive.")
    }
    else{
        println("$number is Negative.")
    }
    val marks = 85
    if (marks >= 90){
        println("Grade: A+")
    }
    else if (marks >= 80){
        println("Grade: A")
    }
    else if (marks >= 70){
        println("Grade: B")
    }
    else {
        println("Grade: C")
    }
}